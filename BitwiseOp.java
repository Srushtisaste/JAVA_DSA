public class BitwiseOp{
    public static void main (String [] args){
        //Binary AND &
        System.out.println(5&6);

         //Binary OR |
        System.out.println(5|6);

         //Binary XOR ^
        System.out.println(5^6);

         //Binary 1's complement ~
        System.out.println(~5);
        
         //Binary left shift <<
        System.out.println(5<<6);

          //Binary right shift>>
        System.out.println(5>>6);
    }
}